<?php

namespace Intechnology\BackendManagementBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Country
 */
class Country
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var string
     */
    private $name;

    /**
     * @var string
     */
    private $printableName;

    /**
     * @var string
     */
    private $isoCode;

    /**
     * @var string
     */
    private $iso3Code;

    /**
     * @var integer
     */
    private $isoNumCode;

    /**
     * @var string
     */
    private $active;

    /**
     * @var string
     */
    private $deleted;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     * @return Country
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string 
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set printableName
     *
     * @param string $printableName
     * @return Country
     */
    public function setPrintableName($printableName)
    {
        $this->printableName = $printableName;

        return $this;
    }

    /**
     * Get printableName
     *
     * @return string 
     */
    public function getPrintableName()
    {
        return $this->printableName;
    }

    /**
     * Set isoCode
     *
     * @param string $isoCode
     * @return Country
     */
    public function setIsoCode($isoCode)
    {
        $this->isoCode = $isoCode;

        return $this;
    }

    /**
     * Get isoCode
     *
     * @return string 
     */
    public function getIsoCode()
    {
        return $this->isoCode;
    }

    /**
     * Set iso3Code
     *
     * @param string $iso3Code
     * @return Country
     */
    public function setIso3Code($iso3Code)
    {
        $this->iso3Code = $iso3Code;

        return $this;
    }

    /**
     * Get iso3Code
     *
     * @return string 
     */
    public function getIso3Code()
    {
        return $this->iso3Code;
    }

    /**
     * Set isoNumCode
     *
     * @param integer $isoNumCode
     * @return Country
     */
    public function setIsoNumCode($isoNumCode)
    {
        $this->isoNumCode = $isoNumCode;

        return $this;
    }

    /**
     * Get isoNumCode
     *
     * @return integer 
     */
    public function getIsoNumCode()
    {
        return $this->isoNumCode;
    }

    /**
     * Set active
     *
     * @param string $active
     * @return Country
     */
    public function setActive($active)
    {
        $this->active = $active;

        return $this;
    }

    /**
     * Get active
     *
     * @return string 
     */
    public function getActive()
    {
        return $this->active;
    }

    /**
     * Set deleted
     *
     * @param string $deleted
     * @return Country
     */
    public function setDeleted($deleted)
    {
        $this->deleted = $deleted;

        return $this;
    }

    /**
     * Get deleted
     *
     * @return string 
     */
    public function getDeleted()
    {
        return $this->deleted;
    }
}
